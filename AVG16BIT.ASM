ASSUME CS:CODE,DS:DATA
DATA SEGMENT
     LIST DW 0200H,0900H,0500H,0400H
     COUNT DW 0004H
     AVG DW ?
DATA ENDS
CODE SEGMENT
START:
        MOV AX,DATA
        MOV DS,AX
        MOV CX,COUNT
        XOR DX,DX
        XOR AX,AX
        MOV SI,OFFSET LIST
AGAIN0: ADD AX,[SI]
        JNC AGAIN
        INC DX
AGAIN:  ADD SI,02
        LOOP AGAIN0
        DIV COUNT
        MOV AVG,AX
        MOV AH,4CH
        INT 21H
CODE ENDS
END START

