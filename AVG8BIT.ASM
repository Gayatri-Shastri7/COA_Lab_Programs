ASSUME CS:CODE,DS:DATA
DATA SEGMENT
     LIST DB 01H,09H,05H,04H
     COUNT DB 04
     AVG DW ?
DATA ENDS
CODE SEGMENT
START:
        MOV AX,DATA
        MOV DS,AX
        MOV CL,COUNT
        XOR DX,DX
        XOR AX,AX
        MOV SI,OFFSET LIST
AGAIN0: ADD AL,[SI]
        JNC AGAIN
        INC DL
AGAIN:  ADD SI,01
        LOOP AGAIN0
        DIV COUNT
        MOV AVG,AX

        MOV AH,4CH
        INT 21H
CODE ENDS
END START

